//byte,short,int,long

public class DataTypes {
    public static void main(String[] args) {
        System.out.println(1_000_000_000_000_000L); //long, default integer
        System.out.println(1.0 / 3.0);  //double
        System.out.println(1.0F / 3.0F); //float
        System.out.println('b'); //char
        System.out.println('?'); //char
        System.out.println('\u2602'); //char
    }
}


